package pong;

/**
 * Provides the vertical directions / positions possible in Pong.
 *
 * The VerticalD enum allows the Pong game to represent the direction of the
 * ball, the positions of the paddles, and other such information as being
 * UP, DOWN, or NEUTRAL (centred position).
 */
public enum VerticalD {
	UP,
	DOWN,
	NEUTRAL
}
