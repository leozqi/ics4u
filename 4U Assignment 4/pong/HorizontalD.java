package pong;

/**
 * Provides the horizontal directions / positions possible in Pong.
 * 
 * The HorizontalD enum allows the Pong game to represent the direction of the
 * ball, the positions of the paddles, and other such information as being
 * LEFT, RIGHT, or NEUTRAL (centred).
 */
public enum HorizontalD {
	LEFT,
	RIGHT,
	NEUTRAL
}
