// Define a class called "HelloWorld"
public class HelloWorld {
	// main function is run first if a class is executed
	public static void main(String [] args)
	{
		// Print "Hello World"
		System.out.println("Hello World");
	}
}

// public: exposes function to other classes
// static: static methods belong to class; not individual object
// void: returns no value
// main: name of method
// (String [] args): takes array of strings a parameter
// { .. }: block of code
